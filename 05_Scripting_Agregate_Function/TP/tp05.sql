--Soal 4
SELECT m.ID_MEMBER,
       m.NAMA,
       FLOOR(MONTHS_BETWEEN(SYSDATE, m.TGL_LAHIR)/12) AS "UMUR",
       COUNT(tr.KODE_PEMESANAN) AS "JUMLAH NONTON"
FROM MEMBER m
JOIN TRANSAKSI tr ON m.ID_MEMBER = tr.ID_MEMBER
WHERE m.TGL_LAHIR = (
    SELECT MAX(TGL_LAHIR)
    FROM MEMBER
)
GROUP BY m.ID_MEMBER, m.NAMA, m.TGL_LAHIR;

--Soal 5
SELECT TO_CHAR(TGL_LAHIR, 'MONTH') AS "BULAN",
       COUNT(*) AS "JUMLAH MEMBER"
FROM MEMBER
GROUP BY TO_CHAR(TGL_LAHIR, 'MONTH')
ORDER BY "BULAN";

