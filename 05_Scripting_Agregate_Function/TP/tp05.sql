SELECT f.JUDUL AS "JUDUL FILM",
       COUNT(j.ID_JADWAL) AS "JUMLAH JADWAL"
FROM FILM f
JOIN JADWAL j ON f.ID_FILM = j.ID_FILM
GROUP BY f.JUDUL;